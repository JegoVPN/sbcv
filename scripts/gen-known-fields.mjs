// Generates src/domain/knownFields.generated.ts — the per-(channel, kind, type) set of valid sing-box
// field names, extracted from the bundled upstream docs (the project's single source of truth). Consumed
// by the unknown-field linter (W9), UNIONED at runtime with the app's handledFields/shared-field defs so
// neither source's gaps cause a false positive (docs occasionally omit a valid field — e.g. tuic
// zero_rtt_handshake; the app's handled sets cover those, and vice-versa).
//
//   node scripts/gen-known-fields.mjs
//
// Re-run whenever docs/upstream/sing-box/** changes. The output is committed (the Vite build can't read
// the docs tree at runtime).

import { readFileSync, writeFileSync, existsSync, readdirSync } from "node:fs";

const CHANNELS = ["stable", "testing"];
const KIND_DIR = {
  inbound: "inbound",
  outbound: "outbound",
  "dns-server": "dns/server",
  "rule-set": "rule-set",
  endpoint: "endpoint",
  service: "service",
};
// All shared-field-group docs (filenames; note dns01_challenge uses an underscore). Spans both channels
// — channel-only groups (http2 / http-client / quic / neighbor are 1.14) simply have no stable file.
const SHARED_DOCS = [
  "dial", "listen", "tls", "http-client", "http2", "quic", "multiplex",
  "v2ray-transport", "udp-over-tcp", "tcp-brutal", "wifi-state", "neighbor",
  "dns01_challenge", "pre-match",
];

function fieldsFrom(file) {
  if (!existsSync(file)) return [];
  // `#### name` headers enumerate an object's fields in the upstream docs.
  return [...new Set([...readFileSync(file, "utf8").matchAll(/^#### ([a-z0-9_]+)/gm)].map((m) => m[1]))];
}

const out = {};
for (const channel of CHANNELS) {
  const root = `docs/upstream/sing-box/${channel}/configuration`;
  if (!existsSync(root)) continue;
  const byKind = {};
  for (const [kind, dir] of Object.entries(KIND_DIR)) {
    const full = `${root}/${dir}`;
    if (!existsSync(full)) continue;
    byKind[kind] = {};
    for (const file of readdirSync(full)) {
      if (!file.endsWith(".md") || file.endsWith(".zh.md") || file === "index.md") continue;
      const type = file.replace(/\.md$/, "");
      const fields = fieldsFrom(`${full}/${file}`);
      if (fields.length) byKind[kind][type] = fields;
    }
  }
  const shared = {};
  for (const g of SHARED_DOCS) {
    const fields = fieldsFrom(`${root}/shared/${g}.md`);
    if (fields.length) shared[g] = fields;
  }
  out[channel] = { byKind, shared };
}

const header = `// AUTO-GENERATED by scripts/gen-known-fields.mjs — DO NOT EDIT BY HAND.
// Per-(channel, kind, type) valid field names extracted from docs/upstream/sing-box/**. Consumed by the
// unknown-field linter (knownFieldsRegistry.ts), unioned with the app's handledFields so neither source's
// gaps over-block. Re-generate with: node scripts/gen-known-fields.mjs
`;
writeFileSync(
  "src/domain/knownFields.generated.ts",
  `${header}\nexport const DOC_FIELD_NAMES = ${JSON.stringify(out, null, 2)} as const;\n`,
);
const counts = CHANNELS.map((c) => `${c}: ${Object.values(out[c]?.byKind ?? {}).reduce((n, t) => n + Object.keys(t).length, 0)} types, ${Object.keys(out[c]?.shared ?? {}).length} shared`).join(" | ");
console.log(`wrote src/domain/knownFields.generated.ts (${counts})`);
